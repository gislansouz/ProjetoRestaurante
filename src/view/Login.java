    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            conex.executaSql("select*from usuario where usuario='"+campousuario.getText()+"'");
            conex.rs.first();
            if(conex.rs.getString("senha").equals(camposenha.getText())){
                TelaPrincipal tela= new TelaPrincipal();
                tela.setVisible(true);
                dispose();
            }else{
                JOptionPane.showMessageDialog(rootPane,"usuario não cadastrado");
            }
        } catch (SQLException ex) {
            System.out.print("usuario nao permitido");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

