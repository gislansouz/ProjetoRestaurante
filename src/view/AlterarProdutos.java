    public AlterarProdutos(String IdProduto) {
        IdProdutors = IdProduto;
        initComponents();
        Camponome.setEnabled(false);
        ComboBoxCategoria.setEnabled(false);
        CampoQuantEstoque.setEnabled(false);
        CampoPrecoCusto.setEnabled(false);
        CampoPrecoVenda.setEnabled(false);
        conex.conexao();
        conex.executaSql("select* from produtos where nome='"+IdProduto+"'");
        try {
            conex.rs.first();
            campoid.setText(String.valueOf(conex.rs.getInt("id")));
            Camponome.setText(conex.rs.getString("nome"));
            ComboBoxCategoria.setSelectedItem(conex.rs.getString("categoria"));
            CampoPrecoCusto.setText(String.valueOf(conex.rs.getString("valor_custo")));
            CampoPrecoVenda.setText(String.valueOf(conex.rs.getString("valor_venda")));
            CampoQuantEstoque.setText(String.valueOf(conex.rs.getInt("quant_estoque")));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,"Erro ao inserir dados:/nErro:"+ex);
        }

    }

